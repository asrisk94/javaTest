package workshop5;

public class Test05 {

	public static void main(String[] args) {
		
		String str = "LGcns";
		
		System.out.println(str.toUpperCase());		// 대문자화
		System.out.println(str.toLowerCase());		// 소문자화
		System.out.println(str.substring(0, 2));	// 앞 2글자 슬라이싱
		System.out.println(str.substring(2));		// 뒤 3글자 슬라이싱
		
	}

}
